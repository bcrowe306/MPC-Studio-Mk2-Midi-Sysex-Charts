def itergetcharfromfontbuf(
        charbuf: list,
        character: str) -> int:
    l = charbuf[0]
    start = ord(character) * l + 1
    end = start + l
    for i in range(start, end):
        yield charbuf[i]


def getcharfont(
        charbuf: list,
        character: str) -> list:
    return list(itergetcharfromfontbuf(charbuf, character))

